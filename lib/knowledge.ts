/**
 * 根据 slug 获取百科文章详情
 */
export function getArticleBySlug(slug: string): KnowledgeArticle | undefined {
  return knowledgeArticles.find((a) => a.slug === slug);
}

/**
 * 获取所有文章 slug 列表（供静态路由生成）
 */
export function getAllArticleSlugs(): string[] {
  return knowledgeArticles.map((a) => a.slug);
}

/**
 * 获取百科文章列表数据（供列表页使用）
 */
export function getArticleList() {
  return knowledgeArticles.map((a) => ({
    slug: a.slug,
    title: a.title,
    excerpt: a.excerpt,
    category: a.category,
    readingTime: a.readingTime,
    seo: a.seo,
  }));
}

/**
 * 获取百科分类列表
 */
export function getCategories() {
  const categories = [...new Set(knowledgeArticles.map((a) => a.category))];
  return categories;
}
